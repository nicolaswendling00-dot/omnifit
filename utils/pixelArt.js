// THÈME 8-BIT : jeu d'icônes et badges de rang en pixel-art.
//
// Chaque icône est décrite par une grille de 12×12 caractères ('#' = plein).
// `pxIcon` fusionne les pixels contigus d'une même ligne en un seul <rect>, ce
// qui garde le SVG compact, et utilise shape-rendering="crispEdges" pour éviter
// tout lissage : l'aspect reste franchement pixelisé à toutes les tailles.

const G = 12; // côté de la grille

// Grille de caractères -> SVG (fusion des séries horizontales)
export function pxIcon(rows, opts = {}) {
  const fill = opts.fill || 'currentColor';
  let rects = '';
  rows.forEach((row, y) => {
    let x = 0;
    while (x < row.length) {
      if (row[x] === '#') {
        let w = 1;
        while (x + w < row.length && row[x + w] === '#') w++;
        rects += `<rect x="${x}" y="${y}" width="${w}" height="1"/>`;
        x += w;
      } else x++;
    }
  });
  return `<svg viewBox="0 0 ${G} ${G}" fill="${fill}" shape-rendering="crispEdges" class="px-icon">${rects}</svg>`;
}

