// Normalise chaque grille en 12 lignes de 12 caractères (sécurité).
function norm(rows) {
  const out = [];
  for (let y = 0; y < G; y++) {
    const r = (rows[y] || '').padEnd(G, '.').slice(0, G);
    out.push(r);
  }
  return out;
}

